#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char *s2;

	s2 = NULL;
	s2 = malloc(ft_strlen((char *)s1) + 1);
	if (s2)
		ft_strcpy(s2, s1);
	return (s2);
}
