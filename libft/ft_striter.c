#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	len;
	int	i;

	i = 0;
	if (s && f)
	{
		len = ft_strlen(s);
		while (len - i)
		{
			f(s + i);
			i++;
		}
	}
}
