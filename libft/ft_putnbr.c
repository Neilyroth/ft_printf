#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	u;

	u = n;
	if (n < 0)
	{
		ft_putchar('-');
		u = -u;
	}
	if (u >= 10)
	{
		ft_putnbr(u / 10);
		ft_putnbr(u % 10);
	}
	else
		ft_putchar('0' + u);
}
